    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
                                           
    // TODO add your handling code here:
    String username = uname.getText();
    String password = new String(pass.getPassword());

    try {
        String sql1 = "select * from admin";
        rs = stmt.executeQuery(sql1);

        boolean found = false;

        while (rs.next()) {
            String dbUsername = rs.getString("username");
            String dbPassword = rs.getString("password");

            if (username.equals(dbUsername) && password.equals(dbPassword)) {
                found = true;
                break; // Exit the loop once a correct match is found
            }
        }

        if (found) {
            uname.setText("");
            pass.setText("");
            JOptionPane.showMessageDialog(this, "Login Successfully");
            this.dispose();
            DashBoard dframe = new DashBoard();
            dframe.setVisible(true);
            dframe.setLocationRelativeTo(null);
        } else {
            JOptionPane.showMessageDialog(this, "Incorrect Username or Password");
        }

    } catch (Exception e) {
        e.printStackTrace(); // It's important to log exceptions for debugging
    }


    }//GEN-LAST:event_jButton2ActionPerformed

